package com.leapfinance.assesment.vo;

public class UserVO {

	private String userId;
	private String password;
	private String url;
	private int saveUrlId;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getSaveUrlId() {
		return saveUrlId;
	}
	public void setSaveUrlId(int saveUrlId) {
		this.saveUrlId = saveUrlId;
	}
	
	
	
	
	
}
